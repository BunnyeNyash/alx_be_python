# hours_to_seconds.py
# Objective: Demonstrate understanding of variable assignments and arithmetic operations by converting a given number of hours into seconds.
# A Python script that converts a specific number of hours into seconds. This task reinforces the concept of arithmetic operations within a practical context.
# The conversion is done by multiplying the number of hours by 3600 (since there are 3600 seconds in an hour; 60 minutes in an hour and 60 seconds in a minute, thus 60 x 60 = 3600).

# Define the number of hours
hours = 2 

# Convert hours to seconds
seconds = hours * 3600

# Print the result
print(f"{hours} hour(s) is {seconds} seconds.")