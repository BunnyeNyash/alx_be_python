# rectangle_area.py
# Objective: Use basic Python arithmetic operations and variable assignments to calculate the area of a rectangle using its length and width.
# A Python script that calculates the area of a rectangle. The area of a rectangle is found by multiplying its length by its width (Area = length * Width).

# Define the length and width of the rectangle
length = 10  # Length of the rectangle in units
width = 5  # Width of the rectangle in units

# Calculate the area of the rectangle
area = length * width

# Print the result
print(f"The area of the rectangle is: {area}")