# sum_numbers.py
# This script calculates the sum of all numbers in a list using a for loop.

# Create a list of numbers
numbers = [1, 5, 3, 9]

# Store the sum of the numbers in the list
total = 0

# Iterate over the numbers in the list summing them up
for number in numbers:
    total += number

# Print sum of all the numbers in the list
print(f"The sum of the numbers is: {total}")
